#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t len)
{
	size_t i;

	i = -1;
	while (++i < len)
		if (((unsigned char*)s)[i] == (unsigned char)c)
			return (void *)&(((unsigned char*)s)[i]);
	return (NULL);
}
