#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t len)
{
	if (dst == NULL && src == NULL)
		return (dst);
	while (len > 0)
	{
		len--;
		((unsigned char*)dst)[len] = ((unsigned char*)src)[len];
	}
	return (dst);
}
