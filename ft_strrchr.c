#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t size;

	size = ft_strlen(s) + 1;
	while (size > 0)
	{
		size--;
		if ((unsigned char)s[size] == c)
			return ((char*)&s[size]);
	}
	return (NULL);
}
